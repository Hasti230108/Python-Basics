Set_A = {5, 10, 15, 20, 25, 30, 35, 40}
Set_B = {10, 22, 30, 44, 50}

#union operator
Set_C = Set_A | Set_B 

#intersection operator
Set_D = Set_A & Set_B

#difference operator
Set_E = Set_A - Set_B

print("Set A: ", Set_A)
print("Set B: ", Set_B)
print("Set C (union): ", Set_C)
print("Set D (intersection): ", Set_D)
print("Set E (difference Set A - Set B): ", Set_E)